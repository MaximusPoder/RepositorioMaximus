    private void attjTableEmpresaProdutosCusto() {
        String codigo = util.separa(1,cbNomeEmpresa.getSelectedItem().toString());

        conexao.execute("select * from empresa_produtos_custo"
                        + " where cod_empresa = " + codigo);

        tbProdutosCustos.getColumnModel().getColumn(0).setMaxWidth(0);
        tbProdutosCustos.getColumnModel().getColumn(0).setPreferredWidth(0);
        tbProdutosCustos.getColumnModel().getColumn(1).setPreferredWidth(10);
        tbProdutosCustos.getColumnModel().getColumn(2).setPreferredWidth(10);
        tbProdutosCustos.getColumnModel().getColumn(3).setPreferredWidth(10);

        DefaultTableModel modelo = (DefaultTableModel)tbProdutosCustos.getModel();
        modelo.setNumRows(0);//limpa o JTable;

        try{
            while (conexao.resultSet.next())
                modelo.addRow(new Object[]{conexao.resultSet.getString("cod_empresa_produtos_custo"),
                                           conexao.resultSet.getString("especie"),
                                           conexao.resultSet.getString("apetrecho"),
                                           conexao.resultSet.getString("proprio_terceiro"),
                                           conexao.resultSet.getString("epoca_ano"),
                                          });

            conexao.resultSet.first();

        }catch (SQLException erro){
            System.out.println(erro + "Tabela Familia Renda Trabalho");
        }
    }

